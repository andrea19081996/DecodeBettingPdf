import pytest
import main

def is_special_team_test():
    assert main.is_special_team('SCHALKE 04') == True

    assert main.is_special_team('TEST') == False

    assert main.is_special_team('FOOTBALL BOURG-EN-BRESSE PERONNAS 01') == True

def find_first_team_test():
    assert main.find_first_team('1234 SCHALKE 04 ') == 'SCHALKE 04 '

    assert main.find_first_team('SCHALKE 04 ') == 'SCHALKE 04 '

    assert main.find_first_team('0 SCHALKE 04 ') == 'SCHALKE 04 '
    assert main.find_first_team('0 SCHALKE 04') == 'SCHALKE 04'


def find_number_quote_match_test():
    assert main.find_number_quote_match('TEST 1,2 1,2 3,1', 1, ['TEST']) == ['TEST', '1,2', '1,2', '3,1']

    assert main.find_number_quote_match('TEST MAIN 1,2 1,2 3,1', 2, ['TEST MAIN']) == ['TEST MAIN', '1,2', '1,2', '3,1']

